// 회원 구분별 포메이션 배정 우선순위 상수
// 숫자가 작을수록 우선순위가 높음(먼저 출전 쿼터 확보).
// 규칙 4: 스텝 > 정회원 > 학생 > 정회원(월2회) > 준회원 > 용병
// ─────────────────────────────────────────────────────────────
import type { MemberType } from "@/types/member";

// 숫자가 작을수록 우선순위가 높음(먼저 3쿼터 확보, 마지막에 컷).
// 인원이 많아 일부가 2쿼터만 뛰어야 할 때:
//   정회원 → 정회원(월2회) → 준회원 → 용병 순으로 컷이 내려간다(준회원/용병부터 2쿼터).
//   스텝·회장·학생은 모두 '정회원'과 동일 등급으로 취급한다(고순위 우대 없음).
export const MEMBER_PRIORITY: Record<MemberType, number> = {
  회장: 2, // 정회원과 동일
  스텝: 2, // 정회원과 동일
  정회원: 2,
  학생: 2, // 정회원과 동일
  "정회원(월2회)": 3,
  준회원: 4,
  용병: 5,
  // 아래는 정상적으로 배정 대상이 아님(참석 시에만 최하위)
  휴식: 90,
  탈퇴: 99,
  기타: 50,
};

/** 회원 구분의 우선순위 점수를 반환(작을수록 우선) */
export function getPriority(memberType: MemberType): number {
  return MEMBER_PRIORITY[memberType] ?? 50;
}

